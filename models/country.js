'use strict';

module.exports = (sequelize, DataTypes) => {
    // Ваша модель страны
    return sequelize.define('countries', {
        id: {
            type: DataTypes.INTEGER,
            allowNull: false,
            primaryKey: true,
            autoIncrement: true
        },
        name: {
            type: DataTypes.TEXT,
            unique: true
        }
    }, {
        indexes: [
            {
                name: 'countries_name',
                fields: ['name']
            }
        ]
    });
};
